void makeRasterFont(void)
{
   GLuint i;
   glPixelStorei(GL_UNPACK_ALIGNMENT, 1);

   m_font_offset = glGenLists (256);
   for (i = 0; i < 256; i++) {
      glNewList(m_font_offset + i, GL_COMPILE);
      glBitmap (8, 16, 0.0, 0.0, 8.0, 0.0, (GLubyte *)(&(m_fontdata_ascii_8x16[i*16])));
      glEndList();
   }
   glEndList();
}

void glutfont_init (void)
{
   glShadeModel (GL_FLAT);
   makeRasterFont ();
}

void glutfont_print (const char *fmt, ...)
{
    char    text[256];
    va_list ap;
    if (NULL == fmt) {
        return;
    }
    va_start (ap, fmt);
    vsprintf (text, fmt, ap);
    va_end (ap);
    glPushAttrib (GL_LIST_BIT);
    glListBase (m_font_offset);
    glCallLists (strlen(text), GL_UNSIGNED_BYTE, (GLubyte *)text);
    glPopAttrib ();
}
